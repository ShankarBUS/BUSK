namespace BUSK.Charting
{
    /// <summary>
    /// Charts legend locations
    /// </summary>
    public enum LegendLocation
    {
        /// <summary>
        /// Disables legend
        /// </summary>
        None,
        /// <summary>
        /// PLaces legend at top
        /// </summary>
        Top,
        /// <summary>
        /// Places legend at bottom
        /// </summary>
        Bottom,
        /// <summary>
        /// Places legend at left
        /// </summary>
        Left,
        /// <summary>
        /// Places legend at right
        /// </summary>
        Right
    }
}
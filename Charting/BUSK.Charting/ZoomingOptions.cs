namespace BUSK.Charting
{
    /// <summary>
    /// Chart zooming options
    /// </summary>
    public enum ZoomingOptions
    {
        /// <summary>
        /// Disables zooming
        /// </summary>
        None,
        /// <summary>
        /// Zoom only at X axis
        /// </summary>
        X,
        /// <summary>
        /// Zoom only at Y axis
        /// </summary>
        Y,
        /// <summary>
        /// Zooms in both, X and Y axes
        /// </summary>
        Xy
    }
}